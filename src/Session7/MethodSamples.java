package Session7;

public class MethodSamples {
    public static void main(String[] args) {
        printMessage();
        showGreeting("Nastia");
    }

    public static void printMessage() {
        System.out.println("Hello, World!");
    }

    public static void showGreeting(String name) {
        System.out.println("Hello, " + name + "!");
    }
}
